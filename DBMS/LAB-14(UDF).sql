DECLARE @N INT
DECLARE @FLAG INT
DECLARE @I INT
SET @N=5
SET @FLAG=0
SET @I=2
BEGIN
	WHILE(@I<=@N/2)
		BEGIN
			IF(@N%@I=0)
				BEGIN
					SET @FLAG=1;
					BREAK;
				END
			SET @I=@I+1
		END
	IF(@FLAG=1)
		PRINT 'NOT PRIME NUMBER';
	ELSE
		PRINT 'PRIME NUMBER';
END

